from django_distribute.containers.rocketsilo import RocketSilo
from django_distribute.data.constants import (
    ITEM_ID,
    ITEM_KEYWORDS,
    ITEM_ROCKET_CAPACITY,
    ITEM_STACK,
    ITEM_WEIGHT,
)
from django_distribute.data.item import Item


def make_item(
    stack_size: int, rocket_capacity: int, item_id: int, *args: list[str] | float
) -> Item:
    """
    Returns a dictionary of an item's attributes.

    Calculates the item's weight using capacity.
    Allows for optional arguments that
    define keywords and or custom weight.

    :param int stack_size: The item's maximum stack size.
    :param int rocket_capacity: The number of items one silo can hold.
    :param int item_id: The unique identifier for the item.
    :param list[str] | float *args: Optional arguments for keywords and
    custom weight.
    :return: The item's attributes.
    :rtype: Item
    """
    keywords: list[str] = []
    if args and isinstance(args[0], list):
        if len(args[0]) > 0:
            keywords = args[0]
    if len(args) > 1 and isinstance(args[1], float):
        weight = args[1]
    else:
        weight = RocketSilo.CAPACITY / rocket_capacity
    return {
        ITEM_STACK: stack_size,
        ITEM_ROCKET_CAPACITY: rocket_capacity,
        ITEM_WEIGHT: weight,
        ITEM_ID: item_id,
        ITEM_KEYWORDS: keywords,
    }


